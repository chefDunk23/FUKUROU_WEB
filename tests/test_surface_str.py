"""
tests/test_surface_str.py
==========================
_surface_str の境界値ユニットテスト。

JV-Data track_code 仕様:
  芝: 10-22 / ダ: 23-29 / 障: 51-59
  先頭1文字では 20-22 が誤判定されるため整数範囲で判定する。
"""
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

import pytest
from api_v2.routers._race_common import _surface_str


@pytest.mark.parametrize("code, expected", [
    # ── 芝（10-22）─────────────────────────────────────────────────────
    (10,    "芝"),
    ("10",  "芝"),
    ("11",  "芝"),
    ("12",  "芝"),
    ("17",  "芝"),
    ("18",  "芝"),
    (22,    "芝"),
    ("22",  "芝"),
    # ★ 旧実装で誤ってダ判定されていた値
    ("20",  "芝"),
    ("21",  "芝"),
    (10.0,  "芝"),   # float 経由でも正しく動作すること

    # ── ダート（23-29）──────────────────────────────────────────────────
    (23,    "ダ"),
    ("23",  "ダ"),
    ("24",  "ダ"),
    (29,    "ダ"),

    # ── 障害（51-59）────────────────────────────────────────────────────
    (51,    "障"),
    ("52",  "障"),
    ("54",  "障"),
    ("55",  "障"),
    ("57",  "障"),
    (59,    "障"),

    # ── 範囲外 → None ───────────────────────────────────────────────────
    ("00",  None),
    ("09",  None),
    (30,    None),   # 30-50 は未定義
    (50,    None),
    (60,    None),
    (None,  None),
    ("",    None),
    ("A3",  None),   # 非数値
])
def test_surface_str(code, expected):
    assert _surface_str(code) == expected
